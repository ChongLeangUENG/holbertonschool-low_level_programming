#include "3-calc.h"
#include <stdio.h>

/**
 * op_add - Additional function
 * @a: Input integer
 * @b: Input integer
 * Return: Sum of 2 int.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction function
 * @a: Int type
 * @b: Int type
 * Return: sum of 2 int.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication function
 * @a: Int
 * @b: Int
 * Return: sum of 2 ints.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: int
 * @b: int
 * Return: sum of 2 ints.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo function
 * @a: Int
 * @b: Int
 * Return: sum of 2 ints.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
